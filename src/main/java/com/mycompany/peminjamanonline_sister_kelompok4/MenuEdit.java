    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        java.lang.String nama = txtNama.getText();
        java.lang.String email = txtEmail.getText();
        java.lang.String password = txtPassword.getText();
        java.lang.String nik = txtNIK.getText();
        java.lang.String kontak = txtKontak.getText();
        Date tanggalLahir = txtTanggalLahir.getDate();
        java.lang.String alamat = txtAlamat.getText();
        java.lang.String jenisKelamin = rbLakiLaki.isSelected() ? "Laki-laki" : "Perempuan";

        try (Connection conn = DatabaseConnection.getConnection()) {
            java.lang.String sql = "UPDATE users SET username = ?, email = ?, password = ?, nik = ?, kontak = ?, tanggal_lahir = ?, alamat = ?, jenis_kelamin = ? WHERE username = ?";
            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, nama);
                pstmt.setString(2, email);
                pstmt.setString(3, password);
                pstmt.setString(4, nik);
                pstmt.setString(5, kontak);
                pstmt.setDate(6, new java.sql.Date(tanggalLahir.getTime()));
                pstmt.setString(7, alamat);
                pstmt.setString(8, jenisKelamin);
                pstmt.setString(9, username.toString()); 

                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah!", "Info", JOptionPane.INFORMATION_MESSAGE);
       
                    
                            DashboardNasabah formMenuNasabah = new DashboardNasabah(nama); // Kirim username ke form MenuEdit
        formMenuNasabah.setVisible(true);
        
                     this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal diubah!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengubah data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

